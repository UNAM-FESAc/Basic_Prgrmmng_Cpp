public class OperadoresArit
    {
      public static void main(String[] args)
      {
        int a= 7, b=3; // se asigna un valor con =
        System.out.println(a + " + " + b +" = " + (a + b) ); //operador binario suma
        System.out.println(a + " - " + b +" = " + (a - b) ); //operador binario resta
        System.out.println(a + " * " + b +" = " + (a * b) ); //operador binario multiplicacion
        System.out.println(a + " / " + b +" = " + (a / b) ); //operador binario division
        System.out.println(a + " % " + b +" = " + (a % b) ); //operador binario modulo
        /*notese que 3 divide exactamente 2 veces al 6 pero deja un residuo de 1*/
      }
    }
