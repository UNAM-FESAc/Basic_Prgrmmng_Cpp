#include <iostream>
//using namespace std;

int main(){
    int a= 7, b=3; // se asigna un valor con =
    std::cout << "a + b = " << (a + b) << std::endl; //operador binario suma
    std::cout << "a - b = " << (a - b) << std::endl; //op bin resta
    std::cout << "a * b = " << (a * b) << std::endl; //op bin multiplicacion
    std::cout << "a / b = " << (a / b) << std::endl; //op bin division
    std::cout << "a % b = " << (a % b) << std::endl; //op bin modulo

    return 0;
}

